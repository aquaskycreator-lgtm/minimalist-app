const FALLBACK = 'ごめんなさい、その質問にはまだ答えられないです💦\n「捨てられない」「収納のコツ」「やる気が出ない」「リバウンドする」などを聞いてみてください😊'

export function findAnswer(query: string): string {
  let bestScore = 0
  let bestAnswer = FALLBACK

  for (const qa of QA_LIST) {
    const score = qa.keywords.filter(k => query.includes(k)).length
    if (score > bestScore) {
      bestScore = score
      bestAnswer = qa.answer
    }
  }

  return bestAnswer
}
