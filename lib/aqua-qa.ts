export const FALLBACK = '少し違う言い方で聞いてみてください😊\n「捨てられない」「続かない」「どこから始める」「服が多い」「冷蔵庫を整理したい」など、気になることを話しかけてください✨'

export function findAnswer(query: string): string {
  // 正規化: スペース除去、全角→半角
  const normalized = query
    .replace(/\s/g, '')
    .replace(/[！-～]/g, s => String.fromCharCode(s.charCodeAt(0) - 0xFEE0))
    .toLowerCase()

  let bestScore = 0
  let bestAnswer = FALLBACK

  for (const qa of QA_LIST) {
    let score = 0
    for (const keyword of qa.keywords) {
      const kw = keyword.replace(/\s/g, '').toLowerCase()
      if (normalized.includes(kw)) {
        // 長いキーワードほど高スコア
        score += kw.length
      }
    }
    if (score > bestScore) {
      bestScore = score
      bestAnswer = qa.answer
    }
  }

  return bestAnswer
}
