// 料理名からカロリーを検索（部分一致対応）
export function lookupCalories(name: string): number | null {
  if (!name.trim()) return null

  const normalized = name.trim()

  // 完全一致
  if (CALORIE_DB[normalized] !== undefined) return CALORIE_DB[normalized]

  // 部分一致（料理名がキーに含まれる、またはキーが料理名に含まれる）
  let bestMatch: { key: string; kcal: number; score: number } | null = null

  for (const [key, kcal] of Object.entries(CALORIE_DB)) {
    let score = 0
    if (normalized.includes(key)) score = key.length * 2
    else if (key.includes(normalized)) score = normalized.length
    if (score > 0 && (!bestMatch || score > bestMatch.score)) {
      bestMatch = { key, kcal, score }
    }
  }

  return bestMatch ? bestMatch.kcal : null
}
